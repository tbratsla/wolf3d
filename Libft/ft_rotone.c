#include "libft.h"

void	ft_rotone(char *av)
{
	int i;

	i = 0;
	while (av[i])
	{
		if (av[i] == 'Z' || av[i] == 'z')
		{
			ft_putchar(av[i] - 25);
			i++;
		}
		if ((av[i] >= 'a' && av[i] <= 'y') || (av[i] >= 'A' && av[i] <= 'Y'))
		{
			ft_putchar(++av[i]);
			i++;
		}
		else
		{
			ft_putchar(av[i]);
			i++;
		}
	}
}
